int	ft_is_prime(int nb)
{
	int	i;
	int	n;

	i = 2;
	n = nb;
	if (nb < 2)
		return (0);
	while (i != nb)
	{
		n = nb % i;
		if (n == 0)
			return (0);
		i++;
	}
	return (1);
}
